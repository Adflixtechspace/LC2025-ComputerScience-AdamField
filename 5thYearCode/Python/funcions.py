def display_poem(man1, man2, thing):
    print("One fine day in the middle of the night, ")
    print(man1, "and", man2,  "got up to fight")
    print("back to back they faced each other, ")
    print("Drew their", thing, "and shot one 'nother")
    print("The End")
display_poem("Gary", "Brian", "Chicken Roll")
